// Hiding and showing first answer
function answerOne() {
    $('.hidden1').hide();
    $('.hidden1').fadeIn(1000);

    $('.hidden1').show();

  $(document).ready(function() {
    $('button').on('click', function() {
        $(this).next.slideToggle(500);
        $(this).toggleClass('hidden1');
    })
  });
}

